fun calculator(angka1:Float,angka2:Float){
    println ("$angka1 + $angka2 = "+ (angka1+angka1))
    println("$angka1 - $angka2 = "+ (angka1-angka1))
    println ("$angka1 : $angka2 = "+angka1/angka1)
    println ("$angka1 x $angka2 = "+angka1*angka1)
}
fun main(args: Array<String>){
    calculator(1.0f,2.0f)
}

