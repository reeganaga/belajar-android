fun calculator(angka1:Float,angka2:Float){
    println ("$angka1 + $angka2 = "+ (angka1+angka2))
    println("$angka1 - $angka2 = "+ (angka1-angka2))
    println ("$angka1 : $angka2 = "+ (angka1/angka2))
    println ("$angka1 x $angka2 = "+ (angka1*angka2))
}
fun main(args: Array<String>){
    calculator(2.0f,4.0f)
}

